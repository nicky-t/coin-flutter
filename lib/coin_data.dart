class CoinData {
  Future getCoinData() async {
    http.Response response = await http.get('$apiUrl$apiKey');

    if (response.statusCode == 200) {
      var decodedData = jsonDecode(response.body);
      var lastPrice = decodedData['rate'];
      return lastPrice;
    } else {
      print('Request failed.');
    }
  }
}
